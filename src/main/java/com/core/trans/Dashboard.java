package com.core.trans;

public class Dashboard {
	private long users;
	
	private long companies;
	
	private long products;
	
	private long contacts;
	
	private long orders;
	
	private long sales;
	
	private long purchases;
	
	private long invoices;
	
	public Dashboard() {}

	public long getUsers() {
		return users;
	}

	public void setUsers(long users) {
		this.users = users;
	}

	public long getCompanies() {
		return companies;
	}

	public void setCompanies(long companies) {
		this.companies = companies;
	}

	public long getProducts() {
		return products;
	}

	public void setProducts(long products) {
		this.products = products;
	}

	public long getContacts() {
		return contacts;
	}

	public void setContacts(long contacts) {
		this.contacts = contacts;
	}

	public long getOrders() {
		return orders;
	}

	public void setOrders(long orders) {
		this.orders = orders;
	}

	public long getSales() {
		return sales;
	}

	public void setSales(long sales) {
		this.sales = sales;
	}

	public long getPurchases() {
		return purchases;
	}

	public void setPurchases(long purchases) {
		this.purchases = purchases;
	}

	public long getInvoices() {
		return invoices;
	}

	public void setInvoices(long invoices) {
		this.invoices = invoices;
	}
}
